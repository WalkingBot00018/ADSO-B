#Esto define una funcion llamada suma con dos parametros, a y b
def suma (a,b):
    #Esta linea devuelve la suma de los dos argumentos, a y b
    return a+b
#Esto define una segunda funcion llamada suma, pero esta vez con tres parametros, a, b, y c
def suma(a,b,c):
    #Esta linea devuelve la suma de los tres argumentos, a, b, y c
    return a+b+c
#Esta linea llama a la función suma con los argumentos 1, 2, y 3 para sumarlos
# y se imprime en la consola usando la función print
print(suma(1,2,3))